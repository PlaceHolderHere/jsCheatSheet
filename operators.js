// Arithmetic Operators - Basic Calculation
// Follows PEMDAS (Basic order of operations)
let a = 10
let b = 3

console.log(a + b) // add
console.log(a - b) // subtraction
console.log(a * b) // Multiplication
console.log(a / b) // Division
console.log(a % b) // Modulo (Returns the Remainder from Division)
console.log(a ** b) // Power/Exponentiation

